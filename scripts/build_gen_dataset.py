#!/usr/bin/env python3
"""Build the Phase-6 generation dataset: PTB-XL SCP labels -> Findings/Impression JSONL.

    python scripts/build_gen_dataset.py                  # full train/val/test -> data/processed/generation/
    python scripts/build_gen_dataset.py --limit 200       # quick partial run
    python scripts/build_gen_dataset.py --split test      # just one split

Each row is one (structured detection input, target report) supervised fine-tuning
example: PTB-XL's SCP-code label set (restricted to human-validated records, so the
target text reflects an actual cardiologist's read, not an autogenerated one) plus a
*measured* heart rate (Pan-Tompkins R-peaks on the Phase-2 preprocessed signal, not a
guess) and demographics, rendered into the two-section report by
`src/generation/templater.py`. Splits follow PTB-XL's official `strat_fold` (the same
folds the Phase-3/4 detector trains on), so a generator trained here never sees a
record the detector was evaluated on.

Output is gitignored (`data/processed/`, reproducible via this script); a small
committed sample + dataset card live in `docs/generation/`.
"""

from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parents[1]))

import numpy as np  # noqa: E402
import wfdb  # noqa: E402

from src.config import (  # noqa: E402
    PROCESSED_DIR,
    PTBXL_DIR,
    ROOT,
    TEST_FOLD,
    TRAIN_FOLDS,
    VAL_FOLD,
)
from src.data.labels import load_database  # noqa: E402
from src.generation.dataset import build_example  # noqa: E402
from src.preprocessing.pipeline import preprocess  # noqa: E402
from src.preprocessing.segmentation import heart_rate_bpm  # noqa: E402

OUT_DIR = PROCESSED_DIR / "generation"
DOC_DIR = ROOT / "docs" / "generation"
SEED = 42


def _fold_filter(split: str):
    return {"train": lambda f: f in TRAIN_FOLDS, "val": lambda f: f == VAL_FOLD,
           "test": lambda f: f == TEST_FOLD}[split]


def measured_heart_rate(row, sampling_rate: int = 100) -> float | None:
    """Real Pan-Tompkins heart rate from the record's own waveform (not a guess)."""
    col = "filename_lr" if sampling_rate == 100 else "filename_hr"
    try:
        signal, _ = wfdb.rdsamp(str(PTBXL_DIR / row[col]))
    except Exception:
        return None
    raw = signal.T.astype(np.float32)
    _, rpeaks = preprocess(raw, fs_in=sampling_rate, fs_out=100)
    if len(rpeaks) < 2:
        return None
    hr = heart_rate_bpm(rpeaks, 100)
    return float(hr) if np.isfinite(hr) else None


def build_split(split: str, df, limit: int | None) -> list[dict]:
    keep = df["strat_fold"].apply(_fold_filter(split)) & df["validated_by_human"]
    sub = df[keep]
    if limit:
        sub = sub.iloc[:limit]
    examples = []
    n = len(sub)
    for i, (ecg_id, row) in enumerate(sub.iterrows()):
        if i % 1000 == 0:
            print(f"  [{split}] {i}/{n}", flush=True)
        hr = measured_heart_rate(row)
        # per-record RNG so confidence sampling is reproducible and independent of
        # iteration order / --limit
        rng = np.random.default_rng(SEED + int(ecg_id))
        row_named = row.copy()
        row_named.name = ecg_id
        ex = build_example(row_named, rng, heart_rate_bpm=hr)
        if ex is not None:
            examples.append(ex)
    return examples


def write_jsonl(rows: list[dict], path: Path) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w") as f:
        for r in rows:
            f.write(json.dumps(r) + "\n")


def write_dataset_card(counts: dict[str, int], code_counts: dict[str, int]) -> None:
    DOC_DIR.mkdir(parents=True, exist_ok=True)
    top = sorted(code_counts.items(), key=lambda kv: -kv[1])[:15]
    lines = [
        "# Phase 6 — Generation dataset card",
        "",
        "Structured-input -> Findings/Impression training data, built from PTB-XL by "
        "`scripts/build_gen_dataset.py`. Regenerate with `make gen-data`.",
        "",
        "## Composition",
        "",
        f"- **train**: {counts.get('train', 0):,} examples",
        f"- **val**: {counts.get('val', 0):,} examples",
        f"- **test**: {counts.get('test', 0):,} examples",
        "",
        "Filtered to `validated_by_human == True` records only, so every target report "
        "reflects an actual cardiologist's confirmed label set, not an autogenerated one. "
        "Splits follow PTB-XL's official `strat_fold` — the same folds "
        "`src/detection/train.py` uses — so a generator trained here never sees a record "
        "the detector was evaluated on.",
        "",
        "## Fields (per JSONL row)",
        "",
        "`ecg_id, codes, age, sex, heart_rate_bpm, heart_axis, confidences, leads, "
        "review_threshold, original_report, findings, impression`. `heart_rate_bpm` is "
        "**measured** — Pan-Tompkins R-peaks run on the record's own Phase-2-preprocessed "
        "signal — not templated from the rhythm label. `original_report` is PTB-XL's own "
        "(German, autogenerated-then-cardiologist-validated) free-text report, carried "
        "through for provenance/spot-checking, not used as a training target. "
        "`confidences` are synthetically sampled per finding (mostly 0.82-0.99, ~15% "
        "sampled 0.30-0.49) so the model sees the \"(requires clinician confirmation)\" "
        "phrasing during training; PTB-XL labels carry no native confidence signal.",
        "",
        "## Most frequent SCP codes in the training split",
        "",
        "| code | description | train count |",
        "|---|---|---:|",
    ]
    from src.data.labels import load_scp_statements

    scp = load_scp_statements()
    for code, n in top:
        desc = scp.loc[code, "description"] if code in scp.index else ""
        lines.append(f"| {code} | {desc} | {n:,} |")
    lines += [
        "",
        "## Known caveats",
        "",
        "- Target text is **template-rendered**, not human-authored — it teaches the "
        "generator the *exact* two-section format and a controlled clinical vocabulary, "
        "but its prose is more formulaic than a cardiologist's. See "
        "`docs/generation/examples_review.md` for a reviewed comparison against PTB-XL's "
        "own human report text.",
        "- `heart_axis` is only surfaced when unambiguous (LAD/RAD and extreme forms); "
        "orientation codes of uncertain clinical framing (AXL/AXR/SAG) and the normal "
        "bucket (MID) are recorded but not templated into the report text.",
        "- Confidence sampling is synthetic — a real detector's calibration differs "
        "(see `docs/baseline/`); the generator should be re-checked against live detector "
        "confidence distributions before production use.",
    ]
    (DOC_DIR / "dataset_card.md").write_text("\n".join(lines) + "\n")


def main() -> int:
    ap = argparse.ArgumentParser(description=__doc__, formatter_class=argparse.RawDescriptionHelpFormatter)
    ap.add_argument("--split", choices=("train", "val", "test", "all"), default="all")
    ap.add_argument("--limit", type=int, default=None, help="cap records per split (debugging)")
    ap.add_argument("--sample-n", type=int, default=20, help="rows to write to the committed sample")
    args = ap.parse_args()

    df = load_database()
    splits = ("train", "val", "test") if args.split == "all" else (args.split,)
    counts, all_code_counts = {}, {}
    for split in splits:
        print(f"building {split}...")
        rows = build_split(split, df, args.limit)
        write_jsonl(rows, OUT_DIR / f"{split}.jsonl")
        counts[split] = len(rows)
        print(f"  -> {OUT_DIR / f'{split}.jsonl'} ({len(rows)} examples)")
        if split == "train":
            for r in rows:
                for c in r["codes"]:
                    all_code_counts[c] = all_code_counts.get(c, 0) + 1

    if args.split == "all":
        sample_path = DOC_DIR / "sample_examples.jsonl"
        train_rows = [json.loads(line) for line in (OUT_DIR / "train.jsonl").open()]
        rng = np.random.default_rng(SEED)
        idx = rng.choice(len(train_rows), size=min(args.sample_n, len(train_rows)), replace=False)
        write_jsonl([train_rows[i] for i in sorted(idx)], sample_path)
        write_dataset_card(counts, all_code_counts)
        print(f"sample -> {sample_path}\ncard -> {DOC_DIR / 'dataset_card.md'}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
